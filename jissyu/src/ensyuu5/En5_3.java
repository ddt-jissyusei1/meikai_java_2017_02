package ensyuu5;

/*
 * 論理型の変数にtrueやfalseを代入して、その値を表示するプログラムを作成せよ。
 */
public class En5_3 {

	public static void main(String[] args) {

		//論理型の値の表示を確認するためにtrueを代入する
		boolean judgeTrue = true;
		//論理型の値の表示を確認するためにfalseを代入する
		boolean judgeFalse = false;

		//judgeTrueの値を出力する
		System.out.println("変数judgeTrueの値は、" + judgeTrue);
		//judgFalseの値を出力する
		System.out.println("変数judgeFalseの値は、" + judgeFalse);


	}

}
